package TestPractice;

public class switchProgram 
{
	
	public static void main(String[] args) {
		
	
	String fruit = "Apple";
	switch (fruit) {
	    case "Apple":
	        System.out.println("Apple is selected");
	        break;
	    case "Banana":
	        System.out.println("Banana is selected");
	        break;
	    default:
	        System.out.println("Unknown fruit");
	}
	
	}

}
